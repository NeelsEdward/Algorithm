	private static List<Integer> getKthPerson1(int k, List<Integer> p, List<Integer> q) {

		ArrayList<Integer> answer = new ArrayList<Integer>();
		Set<Integer> listInValid = new HashSet();
		Map<Integer, Integer> validMap = new HashMap<Integer, Integer>();
		int upperLimit = 100000;
		if (k <= upperLimit) {
			for (Integer qq : q) {
				if (qq <= upperLimit) {
					int peopleCount = 0;
					int index = 0;
					if (!listInValid.isEmpty() && !isValid(listInValid, qq)) {
						answer.add(0);
						continue;
					}
					if (!validMap.isEmpty() && validMap.containsKey(qq)) {
						answer.add(validMap.get(qq));
						continue;
					}
					if (p.size() <= upperLimit) {
						for (int i = 0; i < p.size(); i++) {
							int number = p.get(i);
							if (number <= upperLimit) {
								if (number >= qq) {
									peopleCount++;
									index = i;
								}
								if (peopleCount >= k) {
									break;
								}
								if ((k - peopleCount >= (p.size() - i))) {
									peopleCount = -1;
									break;
								}
							} else {
								answer.add(0);
								break;
							}

						}
						if (peopleCount >= k) {
							answer.add(index + 1);
							validMap.put(qq, (index + 1));
						} else {
							listInValid.add(qq);
							answer.add(0);
						}

					} else {
						answer.add(0);
					}

				} else {
					answer.add(0);
				}
			}

		} else {
			answer.add(0);
		}
		return answer;
	}

	private static boolean isValid(Set<Integer> mapInValid, int numberToBeMatched) {

		boolean valid = true;
		if (mapInValid.contains(numberToBeMatched)) {
			return false;
		}
		for (Integer value : mapInValid) {
			if (numberToBeMatched >= value) {
				valid = false;
				break;
			}
		}
		return valid;
	}

	private static boolean isValidList(Set<Integer> listValid, int numberToBeMatched) {

		boolean valid = true;
		if (listValid.contains(numberToBeMatched)) {
			return false;
		}
		for (Integer value : listValid) {
			if (numberToBeMatched <= value) {
				valid = false;
				break;
			}
		}
		return valid;
	}

	
